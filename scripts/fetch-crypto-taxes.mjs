import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const ENDPOINT = "https://cryptonomadhub-prod-1.onrender.com/regulations";
const DATASET_URL = "https://cryptonomadhub.io/data";
const EXPECTED_COUNTRIES = 199;

function validRate(value) {
  return typeof value === "number" && Number.isFinite(value) ? value : null;
}

function formatRate(value) {
  if (value === null) return null;
  const percentage = value * 100;
  return `${Number.isInteger(percentage) ? percentage : percentage.toFixed(1)}%`;
}

function shortSummary(row) {
  const short = formatRate(validRate(row.crypto_short_rate));
  const long = formatRate(validRate(row.crypto_long_rate));
  const gains =
    short === long
      ? short
        ? `Capital gains are generally taxed at ${short}.`
        : "No capital-gains rate is listed."
      : `Capital gains are ${short ?? "not listed"} short-term and ${long ?? "not listed"} long-term.`;
  const incomeRates = [
    ["staking", formatRate(validRate(row.staking_rate))],
    ["mining", formatRate(validRate(row.mining_rate))],
  ]
    .filter(([, rate]) => rate)
    .map(([kind, rate]) => `${kind} ${rate}`)
    .join(" and ");
  const income = incomeRates ? ` Income rates: ${incomeRates}.` : "";
  return `${gains}${income} Crypto is ${row.crypto_legal_status ?? "unclear"}.`;
}

async function main() {
  const response = await fetch(ENDPOINT, {
    headers: { "User-Agent": "reloka-data-refresh" },
  });
  if (!response.ok) throw new Error(`CryptoNomadHub returned HTTP ${response.status}`);

  const rows = await response.json();
  if (!Array.isArray(rows) || rows.length !== EXPECTED_COUNTRIES) {
    throw new Error(`Expected ${EXPECTED_COUNTRIES} countries, received ${rows.length ?? "invalid data"}`);
  }

  const seen = new Set();
  const regimes = {};
  for (const row of rows.sort((a, b) => a.country_code.localeCompare(b.country_code))) {
    const countryCode = row.country_code;
    if (!/^[A-Z]{2}$/.test(countryCode) || seen.has(countryCode)) {
      throw new Error(`Invalid or duplicate country code: ${countryCode}`);
    }
    seen.add(countryCode);

    const shortTermRate = validRate(row.crypto_short_rate);
    const longTermRate = validRate(row.crypto_long_rate);
    const qualifyingRate = longTermRate ?? shortTermRate;
    const legalStatus = row.crypto_legal_status ?? "unclear";
    regimes[countryCode] = {
      countryCode,
      countryName: row.country_name,
      taxFreeStatus:
        legalStatus === "legal" && (shortTermRate === 0 || longTermRate === 0),
      cryptoFriendly:
        legalStatus === "legal" && qualifyingRate !== null && qualifyingRate <= 0.1,
      shortTermRate,
      longTermRate,
      stakingRate: validRate(row.staking_rate),
      miningRate: validRate(row.mining_rate),
      holdingPeriodMonths:
        Number.isInteger(row.holding_period_months) && row.holding_period_months > 0
          ? row.holding_period_months
          : null,
      legalStatus,
      shortSummary: shortSummary(row),
      sourceUrl: typeof row.source_url === "string" ? row.source_url : null,
      updatedAt: new Date(row.updated_at).toISOString().slice(0, 10),
    };
  }

  const updatedAt = Object.values(regimes)
    .map((regime) => regime.updatedAt)
    .sort()
    .at(-1);
  const output = `// AUTO-GENERATED by scripts/fetch-crypto-taxes.mjs — do not edit by hand.
// Source: CryptoNomadHub Global Cryptocurrency Tax Regulations Database,
// licensed CC BY 4.0. Refresh monthly with: npm run data:crypto

export type CryptoLegalStatus = "legal" | "restricted" | "banned" | "unclear";

export interface CryptoTaxRegime {
  countryCode: string;
  countryName: string;
  taxFreeStatus: boolean;
  cryptoFriendly: boolean;
  shortTermRate: number | null;
  longTermRate: number | null;
  stakingRate: number | null;
  miningRate: number | null;
  holdingPeriodMonths: number | null;
  legalStatus: CryptoLegalStatus;
  shortSummary: string;
  sourceUrl: string | null;
  updatedAt: string;
}

export const CRYPTO_TAX_DATASET_URL = ${JSON.stringify(DATASET_URL)};
export const CRYPTO_TAX_UPDATED_AT = ${JSON.stringify(updatedAt)};
export const CRYPTO_TAX_REGIMES: Record<string, CryptoTaxRegime> = ${JSON.stringify(regimes, null, 2)};
`;

  writeFileSync(join(ROOT, "src/lib/cryptoTax.generated.ts"), output);
  console.log(`Wrote crypto-tax regimes for ${seen.size} countries.`);
}

main();
